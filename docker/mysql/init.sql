CREATE DATABASE IF NOT EXISTS payments CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE payments;

CREATE TABLE IF NOT EXISTS member_profile (
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(10) NOT NULL,
  view_count INT NOT NULL,
  registered_at DATETIME NOT NULL,
  created_at DATETIME(0),
  updated_at DATETIME(0),
  point INT NOT NULL DEFAULT 0,
  INDEX idx_profile_name_id (name ASC, id ASC),
  INDEX idx_profile_view_count_id (view_count DESC, id DESC),
  INDEX idx_profile_registered_at_id (registered_at DESC, id DESC)
);

